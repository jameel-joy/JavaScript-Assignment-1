// 4. Specify an example for all the datatypes in Javascript, store the values in a variable, and verify the type of
// value stored.


// String
let s = "Hello and welcome to my first JS program"
console.log(typeof s, s);

// Number 
let n = 23;
console.log(typeof n , n);

// Boolean
let b = false;
console.log(typeof b, b);

// undefined
let u = undefined;
console.log(typeof u, u);

// Null
let nu = null;
console.log(typeof nu, nu);

// Symbol
let sym = Symbol('Joy Skills')
console.log(typeof sym, sym);

// Array
let arr = [1,2,3,true,"jameel"];
console.log(typeof arr, arr);

// object
let obj = {
    name: "jameel",
    age: 22
}
console.log(typeof obj, obj);