// Comparison operators demonstration

// Equal to (==) operator
let x = 5;
let y = 5;
let result1 = x == y;  // true
let result2 = x == 10; // false

// Not equal to (!=) operator
let result3 = x != y;  // false
let result4 = x != 10; // true

// Greater than (>) operator
let result5 = x > y;   // false
let result6 = x > 2;   // true

// Less than (<) operator
let result7 = x < y;   // false
let result8 = x < 10;  // true

// Greater than or equal to (>=) operator
let result9 = x >= y;   // true
let result10 = x >= 5;  // true
let result11 = x >= 10; // false

// Less than or equal to (<=) operator
let result12 = x <= y;  // true
let result13 = x <= 5;  // true
let result14 = x <= 2;  // false

// Printing results
console.log("x == y:", result1);
console.log("x == 10:", result2);
console.log("x != y:", result3);
console.log("x != 10:", result4);
console.log("x > y:", result5);
console.log("x > 2:", result6);
console.log("x < y:", result7);
console.log("x < 10:", result8);
console.log("x >= y:", result9);
console.log("x >= 5:", result10);
console.log("x >= 10:", result11);
console.log("x <= y:", result12);
console.log("x <= 5:", result13);
console.log("x <= 2:", result14);
